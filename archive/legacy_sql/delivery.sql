    task_id CHAR(5) PRIMARY KEY,
    rider_id CHAR(5) NOT NULL,
    task_status VARCHAR(20) NOT NULL,
    CONSTRAINT fk_delivery_task_rider
        FOREIGN KEY (rider_id) REFERENCES rider(rider_id)
